    [Generator]
    public class EngineerIncrementalSourceGenerator : IIncrementalGenerator
    {
        public void Initialize(IncrementalGeneratorInitializationContext context)
        {
            GeneratorLogging.SetLoggingLevel(LoggingLevel.Fatal);
            // GeneratorLogging.SetLogFilePath("/Users/tom/GitHub/engineer/src/Engineer.Analyzers/EngineerIncrementalSourceGeneratorLog.txt");
            try
            {
                context.RegisterPostInitializationOutput(static postInitializationContext =>
                {
                    // postInitializationContext.AddEmbeddedAttributeDefinition();
                    postInitializationContext.AddSource("EngineerAttributes.g.cs", SourceText.From("""
                // <auto-generated/>

                #nullable enable annotations
                #nullable disable warnings

                // Suppress warnings
                #pragma warning disable CS0612, CS0618, CS9113, CS1591

                using System;
                using Microsoft.CodeAnalysis;

                namespace Engineer
                {
                    [AttributeUsage(AttributeTargets.Class, AllowMultiple = true)]
                    internal sealed class EngineerCodeAttribute : Attribute
                    {
                    }
                }
                """, Encoding.UTF8));
                });

                var compilationProvider = context.CompilationProvider;

                var graphNodeProvider = context
                    .SyntaxProvider
                    .ForAttributeWithMetadataName(
                        "Engineer.EngineerCodeAttribute",
                        predicate: static (node, cancelToken) => HasEngineerAttribute(node),
                        transform: static (ctx, cancelToken) => GetTransform(ctx));

                var combined = graphNodeProvider.Combine(compilationProvider);

                context.RegisterSourceOutput(
                    combined,
                    (sourceProductionContext, source) => Generate(source.Left, source.Right, sourceProductionContext));
            }
            catch (Exception ex)
            {
                GeneratorLogging.LogMessage(ex.ToString(), LoggingLevel.Error);
                throw;
            }
        }

