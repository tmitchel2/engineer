        private static bool HasEngineerAttribute(SyntaxNode node)
        {
            try
            {
                GeneratorLogging.LogMessage($"HasEngineerAttribute");
                return node is ClassDeclarationSyntax classDeclaration && classDeclaration
                    .AttributeLists
                    .SelectMany(a => a.Attributes)
                    .Where(IsEngineerAttribute)
                    .Any();
            }
            catch (Exception ex)
            {
                GeneratorLogging.LogMessage(ex.ToString(), LoggingLevel.Error);
                throw;
            }
        }

        private static bool IsEngineerAttribute(AttributeSyntax attribute)
        {
            GeneratorLogging.LogMessage($"IsEngineerAttribute");
            var name = attribute.Name.GetText().ToString();
            return name == "EngineerCode" || name == "EngineerAttribute";
        }

        private static bool IsEngineerAttribute(AttributeData attribute)
        {
            GeneratorLogging.LogMessage($"IsEngineerAttribute");
            return attribute.AttributeClass?.Name == "EngineerCodeAttribute";
        }

        private static EngineerTransform GetTransform(GeneratorAttributeSyntaxContext ctx)
        {
